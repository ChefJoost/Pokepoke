export function BottomNav({ page, setPage }) {
  const items = [
    { id: 'dashboard', label: 'Dashboard', icon: <HomeIcon /> },
    { id: 'collection', label: 'Collectie', icon: <GridIcon /> },
    { id: 'add', label: 'Toevoegen', icon: <PlusIcon />, isAdd: true },
    { id: 'sets', label: 'Sets', icon: <LayersIcon /> },
    { id: 'wishlist', label: 'Wishlist', icon: <HeartIcon /> },
  ];

  return (
    <nav className="bottom-nav">
      {items.map(item => (
        <button
          key={item.id}
          className={`nav-item ${item.isAdd ? 'add-btn' : ''} ${page === item.id ? 'active' : ''}`}
          onClick={() => setPage(item.id)}
        >
          {item.icon}
          <span>{item.label}</span>
        </button>
      ))}
    </nav>
  );
}

